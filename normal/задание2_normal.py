useranswer = input("Введите дату: ")
useranswer = useranswer.split(".")
date = []
day = ["Первое", "Второе", "Третье", "Четвёртое", "Пятое", "Шестое", "Седьмое", "Восьмое", "Девятое", "Десятое"]
month = ["Января", "Февраля", "Марта", "Апрелья", "Мая", "Июня", "Июля", "Августа", "Сентября", "Октября", "Ноября",
         "Декабрь"]
for i in useranswer:
    date.append(int(i))
print("{} {} {} года.".format(day[date[0]-1],month[date[1]-1],date[2]))
